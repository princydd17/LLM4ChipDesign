module binary_to_bcd_converter (
    input  [4:0] binary_input,
    output [7:0] bcd_output
);
    // Define the BCD conversion table
    localparam BCD_TABLE = {
        8'b0000_0000, // 00000
        8'b0000_0001, // 00001
        8'b0000_0010, // 00010
        8'b0000_0011, // 00011
        8'b0000_0100, // 00100
        8'b0000_0101, // 00101
        8'b0000_0110, // 00110
        8'b0000_0111, // 00111
        8'b0000_1000, // 01000
        8'b0000_1001, // 01001
        8'b0000_1010, // 01010
        8'b0000_1011, // 01011
        8'b0000_1100, // 01100
        8'b0000_1101, // 01101
        8'b0000_1110, // 01110
        8'b0000_1111, // 01111
        8'b0001_0000, // 10000
        8'b0001_0001, // 10001
        8'b0001_0010, // 10010
        8'b0001_0011, // 10011
        8'b0001_0100, // 10100
        8'b0001_0101, // 10101
        8'b0001_0110, // 10110
        8'b0001_0111, // 10111
        8'b0001_1000, // 11000
        8'b0001_1001, // 11001
        8'b0001_1010, // 11010
        8'b0001_1011, // 11011
        8'b0001_1100, // 11100
        8'b0001_1101, // 11101
        8'b0001_1110, // 11110
        8'b0001_1111  // 11111
    };

    // Index into the BCD table based on the binary input
    assign bcd_output[7:4] = BCD_TABLE[binary_input];
    assign bcd_output[3:0] = BCD_TABLE[binary_input + 16];

endmodule
